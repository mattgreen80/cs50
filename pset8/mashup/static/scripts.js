// Add marker for place to map
function addMarker(place)
{
    // get coordinates from place JSON object
    var LatLng = {lat: place["latitude"], lng: place["longitude"]};
    // custom marker icon
    var image = "http://maps.google.com/mapfiles/ms/micons/grn-pushpin.png"
    // instantiate a new marker using position above
    var marker = new google.maps.Marker({
        map: map,
        position: LatLng,
        icon: {
            url: image,
            // position the label (defined below) below the icon
            labelOrigin: new google.maps.Point(0, 45)
        },
        label: {
            text: place.place_name + "," + place.admin_name1,
            color: 'green'
        }
    });

    // Set marker to listen for clicks
    marker.addListener('click', function() {

        // Get articles matching query giving postal code in place as get request to flask /articles route
        let parameters = {
            geo: place.postal_code
        };
        $.getJSON("/articles", parameters, function(data, textStatus, jqXHR) {

            // create object to store unordered list (HTML)
            let list = "<ul>";

            // loop over articles (to a maximum of 5) and add them to list. The \ is used as an escape char for the quotes.
            for (var i = 0; i < 5; i++) {
                list += "<li><a href=" + data[i]["link"] + " target=\"_blank\">" + data[i]["title"] + "</a></li>";
            }
            // add closing list HTML tag
            list += "</ul>";

            // Call showinfo with search results from /articles as the list of links created above (i.e., news articles)
            showInfo(marker, list);
        });
    });
    // add marker to markers array
    markers.push(marker);
}


// Configure application
function configure()
{
    // Update UI after map has been dragged
    google.maps.event.addListener(map, "dragend", function() {

        // If info window isn't open
        // http://stackoverflow.com/a/12410385
        if (!info.getMap || !info.getMap())
        {
            update();
        }
    });

    // Update UI after zoom level changes
    google.maps.event.addListener(map, "zoom_changed", function() {
        update();
    });

    // Configure typeahead
    $("#q").typeahead({
        highlight: false,
        minLength: 1
    },
    {
        display: function(suggestion) { return null; },
        limit: 10,
        source: search,
        templates: {
            suggestion: Handlebars.compile(
                "<div>" +
                "{{place_name}}, {{admin_name1}}, {{postal_code}}" +
                "</div>"
            )
        }
    });

    // Re-center map after place is selected from drop-down
    $("#q").on("typeahead:selected", function(eventObject, suggestion, name) {

        // Set map's center
        map.setCenter({lat: parseFloat(suggestion.latitude), lng: parseFloat(suggestion.longitude)});

        // Update UI
        update();
    });

    // Hide info window when text box has focus
    $("#q").focus(function(eventData) {
        info.close();
    });

    // Re-enable ctrl- and right-clicking (and thus Inspect Element) on Google Map
    // https://chrome.google.com/webstore/detail/allow-right-click/hompjdfbfmmmgflfjdlnkohcplmboaeo?hl=en
    document.addEventListener("contextmenu", function(event) {
        event.returnValue = true;
        event.stopPropagation && event.stopPropagation();
        event.cancelBubble && event.cancelBubble();
    }, true);

    // Update UI
    update();

    // Give focus to text box
    $("#q").focus();
}


// Remove markers from map
function removeMarkers()
{
    // removes markers from map (but still in array)
    for (var i = 0; i < markers.length; i++) {
        markers[i].setMap(null);
    }
    // deletes markers from the array
    markers = [];
}


// Search database for typeahead's suggestions
function search(query, syncResults, asyncResults)
{
    // Get places matching query (asynchronously)
    let parameters = {
        q: query
    };
    $.getJSON("/search", parameters, function(data, textStatus, jqXHR) {

        // Call typeahead's callback with search results (i.e., places)
        asyncResults(data);
    });
}


// Show info window at marker with content
function showInfo(marker, content)
{
    // Start div
    let div = "<div id='info'>";
    if (typeof(content) == "undefined")
    {
        // http://www.ajaxload.info/
        div += "<img alt='loading' src='/static/ajax-loader.gif'/>";
    }
    else
    {
        div += content;
    }

    // End div
    div += "</div>";

    // Set info window's content
    info.setContent(div);

    // Open info window (if not already open)
    info.open(map, marker);
}


// Update UI's markers
function update()
{
    // Get map's bounds
    let bounds = map.getBounds();
    let ne = bounds.getNorthEast();
    let sw = bounds.getSouthWest();

    // Get places within bounds (asynchronously)
    let parameters = {
        ne: `${ne.lat()},${ne.lng()}`,
        q: $("#q").val(),
        sw: `${sw.lat()},${sw.lng()}`
    };
    $.getJSON("/update", parameters, function(data, textStatus, jqXHR) {

       // Remove old markers from map
       removeMarkers();

       // Add new markers to map
       for (let i = 0; i < data.length; i++)
       {
           addMarker(data[i]);
       }
    });
};
